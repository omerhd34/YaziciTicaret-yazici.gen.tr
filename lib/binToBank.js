// Kart numarasından ilk 6 haneyi (BIN) alır; maskeli (***) veya kısa numarada null döner. 
// Boşluk kabul edilir.
export function getBankNameFromBin(cardNumber) {
 if (!cardNumber || typeof cardNumber !== "string") return null;
 if (/[*•]/.test(cardNumber)) return null;
 const cleaned = cardNumber.replace(/\D/g, "");
 if (cleaned.length < 6) return null;
 const bin = cleaned.substring(0, 6);
 return BIN_TO_BANK[bin] || null;
}
